    public LevelController(Level level, LevelView levelView) {
        this.level = level;
        this.levelView = levelView;
        level.prepareTiles(levelOne);

        for (int i = 0; i < level.getLevel().length; i++) {
            for (int j = 0; j < level.getLevel()[i].length; j++) {
                if (level.getLevel()[i][j] != null) {
                    //System.out.println(level.getLevel()[i][j].getLocation());
                    levelView.addTile(level.getLevel()[i][j]);
                }
            }
        }
    }

    public void update() {
        for (int i = 0; i < level.getLevel().length; i++) {
            for (int j = 0; j < level.getLevel()[i].length; j++) {
                if (level.getLevel()[i][j] != null) {
                    level.getLevel()[i][j].update();
                }
            }
        }
    }
